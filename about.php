    <script>
        // Lightbox for brochure images
        document.addEventListener('DOMContentLoaded', function() {
            var modal = document.getElementById('brochureModal');
            var modalImg = document.getElementById('brochureModalImg');
            var closeBtn = document.getElementById('brochureModalClose');
            // Attach click to all brochure images
            document.querySelectorAll('.product-left-image img').forEach(function(img) {
                img.style.cursor = 'zoom-in';
                img.addEventListener('click', function() {
                    modal.style.display = 'flex';
                    modalImg.src = this.src;
                    modalImg.alt = this.alt;
                    document.body.style.overflow = 'hidden';
                });
            });
            // Close modal on cross click
            closeBtn.onclick = function() {
                modal.style.display = 'none';
                modalImg.src = '';
                document.body.style.overflow = '';
            };
            // Close modal when clicking outside the image
            modal.addEventListener('click', function(e) {
                if (e.target === modal) {
                    closeBtn.onclick();
                }
            });
            // Optional: Esc key to close
            document.addEventListener('keydown', function(e) {
                if (e.key === 'Escape' && modal.style.display === 'flex') {
                    closeBtn.onclick();
                }
            });
        });
    </script>
